package calculadora;

/**
 *
 * @author Álvaro Prieto Barón
 */
public class Operaciones {
    public static float suma(float lh, float rh){
        return lh + rh;
    }
    
    public static float resta(float lh, float rh){
        return lh - rh;
    }
    
    public static float mult(float lh, float rh){
        return lh * rh;
    }
    
    public static float div(float lh, float rh){
        return lh / rh;
    }
}
